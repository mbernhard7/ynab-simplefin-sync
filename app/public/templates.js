// Templates for the files provisioned into a user's sync repository.
// Pure module: no DOM, no globals — imported by both the browser app and the tests.

/**
 * Workflow for the generated repo. The Slack and archive steps are always present but
 * no-op unless their secret exists, so either feature can be enabled or disabled later
 * by adding or removing the secret — no workflow edit needed.
 */
export const syncWorkflowYaml = () =>
    [
        "name: YNAB SimpleFIN Sync",
        "",
        "on:",
        "  schedule:",
        '    - cron: "0 */2 * * *"',
        "  workflow_dispatch:",
        "    inputs:",
        "      dry_run:",
        '        description: "Print the plan without writing to YNAB"',
        "        type: boolean",
        "        default: false",
        "      force:",
        '        description: "Bypass the large-adjustment safety guard"',
        "        type: boolean",
        "        default: false",
        "",
        "permissions:",
        "  actions: write # lets the keepalive step reset the schedule's inactivity timer",
        "  contents: write # lets the optional archive step commit back into this repository",
        "",
        "jobs:",
        "  sync:",
        "    runs-on: ubuntu-latest",
        "    steps:",
        "      - uses: actions/checkout@v5",
        "",
        "      - uses: actions/setup-node@v5",
        "        with:",
        "          node-version: 22",
        "",
        "      - name: Sync",
        "        id: sync",
        "        shell: bash",
        "        env:",
        "          YNAB_API_TOKEN: ${{ secrets.YNAB_API_TOKEN }}",
        "          YNAB_BUDGET_ID: ${{ secrets.YNAB_BUDGET_ID }}",
        "          SIMPLEFIN_ACCESS_URL: ${{ secrets.SIMPLEFIN_ACCESS_URL }}",
        "          SIMPLEFIN_MAP: ${{ secrets.SIMPLEFIN_MAP }}",
        "          SIMPLEFIN_ARCHIVE_ACCOUNTS: ${{ secrets.SIMPLEFIN_ARCHIVE_ACCOUNTS }}",
        "          ARCHIVE_DIR: ${{ secrets.SIMPLEFIN_ARCHIVE_ACCOUNTS != '' && 'archive' || '' }}",
        "          DRY_RUN: ${{ inputs.dry_run && '1' || '' }}",
        "        run: |",
        "          set -o pipefail",
        "          npx ynab-simplefin-sync@3 sync ${{ inputs.force && '--force' || '' }} 2>&1 | tee sync.log",
        "",
        "      # No-op unless the SIMPLEFIN_ARCHIVE_ACCOUNTS secret selects accounts to archive.",
        "      - name: Commit archive",
        "        if: always()",
        "        shell: bash",
        "        env:",
        "          ARCHIVING: ${{ secrets.SIMPLEFIN_ARCHIVE_ACCOUNTS }}",
        "        run: |",
        '          if [ -z "$ARCHIVING" ] || [ ! -d archive ]; then exit 0; fi',
        '          git config user.name "github-actions[bot]"',
        '          git config user.email "41898282+github-actions[bot]@users.noreply.github.com"',
        "          git add archive",
        "          if git diff --staged --quiet; then",
        '            echo "No archive changes."',
        "          else",
        '            git commit -m "archive $(date -u +%F)"',
        "            git push",
        "          fi",
        "",
        "      # No-op unless the SLACK_WEBHOOK_URL secret is set. One line when nothing",
        "      # changed, the full log otherwise.",
        "      - name: Notify Slack",
        "        if: always()",
        "        shell: bash",
        "        env:",
        "          SLACK_WEBHOOK_URL: ${{ secrets.SLACK_WEBHOOK_URL }}",
        "          SYNC_OUTCOME: ${{ steps.sync.outcome }}",
        "        run: |",
        '          if [ -z "$SLACK_WEBHOOK_URL" ]; then exit 0; fi',
        '          if [ "$SYNC_OUTCOME" = "success" ]; then emoji="✅"; else emoji="❌"; fi',
        "          now=\"$(date -u +'%Y-%m-%d %H:%M UTC')\"",
        "          summary=\"$(grep -oE 'applied=[0-9]+ failed=[0-9]+ blocked=[0-9]+' sync.log | tail -1 || true)\"",
        "          nums=\"$(printf '%s' \"$summary\" | grep -oE '[0-9]+' | paste -sd' ' - || true)\"",
        '          if [ -n "$summary" ] && [ "$nums" = "0 0 0" ]; then',
        "            jq -n --arg t \"$emoji YNAB SimpleFIN sync — $now — no balance changes\" '{text: $t}' \\",
        "              | curl -sS -X POST -H 'Content-type: application/json' --data @- \"$SLACK_WEBHOOK_URL\"",
        "          else",
        "            {",
        "              printf '%s YNAB SimpleFIN sync — %s (%s)\\n```\\n' \"$emoji\" \"$now\" \"$SYNC_OUTCOME\"",
        '              cat sync.log 2>/dev/null || echo "(no log captured)"',
        "              printf '\\n```'",
        "            } > slack-message.txt",
        "            jq -Rs '{text: .}' slack-message.txt \\",
        "              | curl -sS -X POST -H 'Content-type: application/json' --data @- \"$SLACK_WEBHOOK_URL\"",
        "          fi",
        "",
        "      # GitHub disables scheduled workflows after 60 days without repo activity;",
        "      # re-enabling our own workflow on every run resets that timer.",
        "      - name: Keep schedule alive",
        "        if: always()",
        "        run: |",
        "          curl -sf -X PUT \\",
        '            -H "Authorization: Bearer ${{ secrets.GITHUB_TOKEN }}" \\',
        '            -H "Accept: application/vnd.github+json" \\',
        '            "${{ github.api_url }}/repos/${{ github.repository }}/actions/workflows/sync.yml/enable"',
        "",
    ].join("\n");

