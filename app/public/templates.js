/** Builds the SIMPLEFIN_MAP value from mapping pairs. */
export const buildMapValue = (mappings) =>
    mappings
        .filter((m) => m.simplefinIds.length > 0)
        .map((m) => `${m.ynabAccountId}=${m.simplefinIds.join("+")}`)
        .join(";");
