    "idempotent: repeated runs the same day amend a single transaction.",
    "",
    "## First run",
    "",
    "The first real sync posts a catch-up adjustment covering existing drift, which may",
    "trip the large-adjustment safety guard (shown as a red run with `blocked`). Check the",
    "numbers in the run log, then re-run once with force: **Actions → YNAB SimpleFIN Sync →",
    "Run workflow → force**.",
    "",
    "## Configuration",
    "",
    "| Secret | Purpose |",
    "|---|---|",
    "| `SIMPLEFIN_ACCESS_URL` | SimpleFIN credential (rotate at the SimpleFIN Bridge) |",
    "| `YNAB_API_TOKEN` | YNAB personal access token |",
    "| `YNAB_BUDGET_ID` | The budget to sync |",
    "| `SIMPLEFIN_MAP` | Account mapping: `<ynabId>=ACT-1+ACT-2;<ynabId2>=ACT-3` |",
    "",
    "## Optional features",
    "",
    "| Secret | Enables |",
    "|---|---|",
    "| `SLACK_WEBHOOK_URL` | A Slack message per run: one line when nothing changed, the full log when a balance moves or a run fails |",
    "| `SIMPLEFIN_ARCHIVE_ACCOUNTS` | Archiving: `all`, or ids like `ACT-1;ACT-2`. Each selected account's raw SimpleFIN data — including full transaction history — is committed to `archive/` in this private repository whenever SimpleFIN refreshes it |",
    "",
    "Add, change, or remove any secret under **Settings → Secrets and variables → Actions**;",
    "removing an optional secret turns its feature off. To change the schedule, edit the",
    "`cron` line in `.github/workflows/sync.yml`. SimpleFIN allows about 24 requests per",
    "day; each run makes one.",
    "",
